let currentLang = localStorage.getItem('wc-lang') || 'zh';

function t(key) {
  return I18N[currentLang]?.[key] || I18N.en[key] || key;
}

function toggleLang() {
  currentLang = currentLang === 'zh' ? 'en' : 'zh';
  localStorage.setItem('wc-lang', currentLang);
  updateUIText();
  populateFilters();
  populateTimezoneSelect();
  refreshCurrentTab();
}

